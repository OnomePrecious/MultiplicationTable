public class MenstrualCycle{
	private int age;
	private int lastPeriodDate;
	private int nextPeriodDate;
	private int ovulationDate;
	

public void setAge(int age){
	this.age = age;
}

public void setLastPeriodDate(int lastPeriodDate){
	this.lastPeriodDate = lastPeriodDate;
}
public void setNextPeriodDate(int nextPeriodDate){
	this.nextPeriodDate = nextPeriodDate;
}
public void setOvulationDate(int ovulationDate){
	this.ovulationDate = ovulationDate;
}

public int getAge(){
	return age;
}

public int getLastPeriodDate(){
	return lastPeriodDate;
}
public int getNextPeriodDate(){
	return nextPeriodDate;
}
public int getOvulationDate(){
	return ovulationDate;
}

}







 
